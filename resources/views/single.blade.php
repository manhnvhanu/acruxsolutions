        <p>Trong chuyến sang thăm một Trung tâm phòng chống ung thư ở Mỹ đang làm về nấm đông trùng hạ thảo, nhóm khoa học thuộc Trung tâm đấu tranh sinh học (Viện Bảo vệ thực vật) cho rằng Việt Nam cũng có thể tạo ra loại nấm này và từ năm 2011 Trung tâm bắt đầu thực hiện và đến nay đã thành công.</p>
        <p>Nhiều người cho rằng, đông trùng hạ thảo là sinh vật đặc biệt, ở dạng côn trùng vào mùa đông, còn mùa hè thì hóa thành cây cỏ nên mới có tên như vậy. Tuy nhiên theo giới khoa học, đông trùng hạ thảo là tên gọi chung cho một nhóm nấm ký sinh và gây bệnh trên côn trùng. Cuối mùa thu đầu đông, chúng ký sinh gây bệnh trên côn trùng. Đến mùa hạ, khi nhiệt độ tăng lên, nấm phát sinh thành quả, mọc thành dạng cây cỏ.</p>
        <p>Trên thế giới, đông trùng hạ thảo khai thác ngoài tự nhiên rất hiếm, chủ yếu ở độ cao 3.200 mét thuộc dãy núi Himalaya và vùng Tây Tạng. Mỗi năm sản lượng thu được chỉ 80 kg nên giá thành rất cao từ 1,2 đến 1,6 tỷ đồng một kg. Trên thị trường Hà Nội, nấm đông trùng hạ thảo chủ yếu là do nuôi trồng.</p>
        <p>Nhóm khoa học Viện Bảo vệ thực vật đã nhập giống nấm với chi phí hơn 1.000 USD một mẫu để nhân nuôi và nghiên cứu. </p>
        <p>Tiến sĩ Phạm Văn Nhạ, thành viên nhóm nghiên cứu cho biết, đông trùng hạ thảo chứa rất nhiều giá trị dinh dưỡng cao, trong đó nhóm tập trung vào hai hợp chất chính là Cordycepin có chức năng phòng chống u xơ, tiền ung thư và Adenosin là thuốc điều trị trong tim mạch.</p>
        <p>Sau khi nhập giống về, nhóm tập trung nghiên cứu công nghệ nuôi trồng, trong đó đi sâu tìm hiểu về thành phần môi trường dinh dưỡng; các yếu tố tác động như sinh thái, thời tiết, nhiệt độ, ánh sáng; từ đó tạo điều kiện trong phòng thí nghiệm phù hợp cho nấm phát triển. </p>
