            Các chuyên gia cảnh báo, hiện trên thị trường có tới 70% là hàng giả nấm đông trùng hạ thảo và đối tượng sử dụng thủ đoạn ngày càng tinh vi hơn nên khó có thể phân biệt hàng thật giả. </p>
